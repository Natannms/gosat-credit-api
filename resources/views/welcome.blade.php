<p>Executa o aplicativo no modo de desenvolvimento. Abra <a href="http://localhost:3000/">http://localhost:3000</a> para visualizá-lo em seu navegador.</p>
<p>A página será recarregada sempre que você fizer alterações. Você também pode ver quaisquer erros de lint no console.</p>
